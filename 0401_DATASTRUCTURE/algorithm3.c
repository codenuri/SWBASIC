#include <stdio.h>

int main()
{
    int j = 0;
    int i = 0;

    int x[10] = { 50, 30, 70, 20, 90, 80, 40, 60, 100, 10};

    for ( j = 0; j < 9; j++ )
    {
        for ( i = j+1; i < 10; i++)
        {
            if ( x[j] > x[i])
            {
                int temp = x[j];
                x[j] = x[i];
                x[i] = temp;
            }
        }
    }


    //---------
    for ( i = 0; i < 10; i++)
        printf("%d\n", x[i]);

    return 0;
}
